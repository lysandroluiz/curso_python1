# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo
print(11) # int
print(-11) # int
print(0)
# Qualquer numero abaixo de zero ou acima de zero é considerado int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11)
print(0.0, -1.5)

# A funçao type mostra o tipo que o python
# inferiu ao valor 
print(   type('Phurgo'))
print(   type(10))
print(   type(-10))
print(type(-10), type(0.0), type(1.1))

